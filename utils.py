def colored(text, color):
    colors = {
        "dark_red": "\033[31m",
        "red": "\033[91m",
        "green": "\033[92m",
        "yellow": "\033[93m",
        "orange": "\033[38;5;208m",
        "reset": "\033[0m"
    }
    return colors.get(color, "") + text + colors["reset"]

